package com.bocom.dto;

import com.bocom.dto.Office;


public class UserOrgRespDto {
	private boolean success;
	private String message;
	private Office data;

	public boolean isSuccess() {
		return success;
	}

	public void setSuccess(boolean success) {
		this.success = success;
	}

	public String getMessage() {
		return message;
	}

	public UserOrgRespDto(boolean success, String message,
	        Office data) {
		super();
		this.success = success;
		this.message = message;
		this.data = data;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public Office getData() {
		return data;
	}

	public void setData(Office data) {
		this.data = data;
	}

	public UserOrgRespDto() {
		super();
	}
}